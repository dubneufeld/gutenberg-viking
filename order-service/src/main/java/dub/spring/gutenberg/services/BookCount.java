package dub.spring.gutenberg.services;

public class BookCount {
	
	private int bookId;
	
	private int count;

	public int getBookId() {
		return bookId;
	}

	public void setBookId(int bookId) {
		this.bookId = bookId;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}	
}
